public class ComparisonOperators {
    public static void main(String[] args) {
        // Vergleichsoperationen funktionieren wie in C#
        int x = 1;
        int y = 2;

        System.out.println(x == y);
        System.out.println(x != y);

        System.out.println(x > y);
        System.out.println(x < y);

        System.out.println(x >= y);
        System.out.println(x <= y);
    }
}
